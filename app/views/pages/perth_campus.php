<h1 class="headiing1 headiing-p">Perth</h1>
 
<div class="melbourne">
<h2>Climate</h2>   
<p>Perth has a Mediterranean climate with warm, sunny weather for most of the year. Summers last from December to February and are generally hot and dry. Winters are from June to August and are relatively cool and wet.</p>
</div>

<div class="melbourne">
<h2>Economy</h2>   
<p>WA is enjoying economic prosperity. Massive investments come into Perth in mining, energy, construction, technology and research. Did you know that you are more likely to get a job in Western Australia than anywhere else in Australia? Western Australian’s are also amongst the highest paid in the country!</p>
</div>

<div class="melbourne">
<h2>Education</h2>   
<p>Education in Perth is internationally recognised for high quality. The qualifications you receive in Western Australia will be recognised by employers and leading education institutions around the world.</p>
</div>

<div class="melbourne">
<h2>Attractions</h2>   
<p>Kings Park & Botanic Gardens, Bell Tower, Perth Zoo, Perth Mint, Adventure World, Fremantle Prison, Crown Casino, Cottesloe Beach, Scarborough Beach, Rottnest Island, Penguin Island, The Pinnacles, Caversham Wildlife Park, Scitech Discovery Centre, Museum of Western Australia, Art Gallery of WA, Mundaring Weir, Avon Valley, Swan Valley, Margaret River, Yanchep National Park.</p>
</div>

<div class="melbourne">
<h2>Accommodation</h2>   
<p>The Perth Campus is located in the Perth CBD and is well connected to public transport. When looking for accommodation, consider a location that is close to a train station or 
buses that stop at the CBD. Renting in Perth is competitive and you will need to make sure you have done some research before you start looking.</p>
</div>

<div class="melbourne">
<h2>Transport</h2>   
<p>Perth’s Public Transport System is divided into travel ‘zones’ and is designed to be easy to use for newcomers. International students are entitled to a 40 percent discount on all public transport throughout WA.</p>
</div>

<div class="melbourne">
<h2>Cost of Living</h2>   
<p>The following information provides an indication of what living expenses are associated with residence in Perth for a single person:</p>
<ul class="li-listing">
<li>Renting a room: $175-$220 per week</li>
<li>Part-time job up to 20 hours a week: $100-$350</li>
<li>Groceries: $60-$80 a week</li>
<li>Pre-paid mobile phone contract: around $30 per month</li>
<li>1 litre petrol: $1.48 - $1.61</li>
</ul>
</div>

<div class="melbourne">
<h2>Food</h2>   
<p>Fantastic food is found everywhere in Perth. It is teaming with an eclectic mix of cafes, cool bars, fresh seafood, and eateries from every part of the world such as authentic Thai, Chinese, Indonesian, Italian, Vietnamese, Indian, Korean and Japanese restaurants. The latest and popular eats in Perth CBD are the Jamie’s Italian and Brookfield Place.</p>
</div>




        
        
            
